const Sequelize = require('sequelize');
module.exports = function(sequelize, DataTypes) {
	return sequelize.define('users', {
		user_id: {
			type: DataTypes.CHAR(20),
			allowNull: false,
			primaryKey: true
		},
		password: {
			type: DataTypes.CHAR(60),
			allowNull: false
		},
		fullname: {
			type: DataTypes.STRING(50),
			allowNull: false
		},
		address: {
			type: DataTypes.STRING(50),
			allowNull: false
		},
		phone: {
			type: DataTypes.CHAR(11),
			allowNull: false
		},
		email: {
			type: DataTypes.STRING(50),
			allowNull: false
		},
		isBlock: {
			type: DataTypes.BOOLEAN,
			allowNull: false,
			defaultValue: 0
		}
	}, {
		sequelize,
		tableName: 'users',
		timestamps: false,
		indexes: [
			{
				name: "PRIMARY",
				unique: true,
				using: "BTREE",
				fields: [
					{ name: "user_id" },
				]
			},
		]
	});
};
